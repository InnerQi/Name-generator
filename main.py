# Simple band name generator #
# Asks user for city they grew up in and name of pet
# Program generators a band name based on the inputs given.

print("Welcome to the band name generator.")
city = input("What city did you grow up in?\n")   # saves input into a variable
pet = input("What is the name of your pet?\n")    # saves input into a variable
print("Your band name is " + city + " " + pet)    # Use string concatenation


